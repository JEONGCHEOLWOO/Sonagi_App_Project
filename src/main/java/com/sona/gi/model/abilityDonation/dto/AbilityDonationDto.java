package com.sona.gi.model.abilityDonation.dto;

public class AbilityDonationDto {
    private String adName;
    private String managerName;
    private String phoneNum;
    private String introduction;
    private String donationDate;
    private String progressTime;
    private String currHc;
    private String place;
    private String sharingContents;

    public String getAdName() {
        return adName;
    }

    public void setAdName(String adName) {
        this.adName = adName;
    }

    public String getManagerName() {
        return managerName;
    }

    public void setManagerName(String managerName) {
        this.managerName = managerName;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getDonationDate() {
        return donationDate;
    }

    public void setDonationDate(String donationDate) {
        this.donationDate = donationDate;
    }

    public String getProgressTime() {
        return progressTime;
    }

    public void setProgressTime(String progressTime) {
        this.progressTime = progressTime;
    }

    public String getCurrHc() {
        return currHc;
    }

    public void setCurrHc(String currHc) {
        this.currHc = currHc;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getSharingContents() {
        return sharingContents;
    }

    public void setSharingContents(String sharingContents) {
        this.sharingContents = sharingContents;
    }
}
